                      <p style="text-align: justify;">Presente em mais de 80 países, a ação marista mantém escolas, universidades, unidades sociais, centros de evangelização, editoras, veículos de comunicação e hospitais. São aproximadamente 3.500 Irmãos que partilham suas tarefas de maneira direta com 72 mil Leigos, promovendo a missão nos cinco continentes, beneficiando mais de 654 mil crianças.</p>
                      <p style="text-align: justify;">No Brasil, os primeiros Irmãos Maristas chegaram em 1897 dispostos a dar continuidade ao projeto de formar bons cristãos e virtuosos cidadãos. O Brasil Marista está presente em 23 estados e no Distrito Federal. São 98 cidades brasileiras, mais de 27 mil Irmãos, Leigas, Leigos e colaboradores, mais de 80 mil alunos distribuídos em unidades de educação básica, mais de 58 mil alunos em unidades de ensino superior, mais de 2 milhões atendimentos somente pelo Sistema Único de Saúde (SUS) e mais de 30 mil pessoas beneficiadas na solidariedade.</p>
                      <p style="text-align: justify;">O Brasil Marista divide-se em três unidades administrativas: Província Marista Brasil Centro-Norte, Província Marista Brasil Sul-Amazônia (Rede Marista) e Província Marista Brasil Centro-Sul (Grupo Marista). O apoio administrativo se dá por meio de mantenedoras, estruturas responsáveis pelo gerenciamento nas diversas frentes de atuação. A União Marista do Brasil (UMBRASIL) é a associação das mantenedoras, representando o universo Marista no país.</p>
                      <p style="text-align: justify;">As 10 associadas têm, atualmente, cerca de 30 mil colaboradores.</p>
